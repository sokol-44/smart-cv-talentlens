/**
 * Gets custom translations map from localStorage.
 * Design Pattern: Registry/Cache Store reader.
 * Design Decision: Prevents application boot crashes by catching localStorage JSON parsing syntax exceptions.
 */
export function getCustomTranslations(): Record<string, string> {
  try {
    const stored = localStorage.getItem("cv_custom_translations");
    return stored ? JSON.parse(stored) : {};
  } catch (e) {
    // Explanation: Corrupted or invalid JSON data in localStorage is safely disregarded,
    // returning an empty dictionary to ensure fallback translations still work.
    console.warn("Custom translations parse error, falling back to empty map:", e);
    return {};
  }
}

/**
 * Saves a custom translation to localStorage.
 * Design Pattern: Registry/Cache Store writer.
 */
export function saveCustomTranslation(original: string, translation: string) {
  if (!original) return;
  try {
    const custom = getCustomTranslations();
    custom[original] = translation;
    localStorage.setItem("cv_custom_translations", JSON.stringify(custom));
  } catch (e) {
    // Explanation: Log writing/quota errors from localStorage safely to avoid disrupting user workflow.
    console.error("Failed to save custom translation:", e);
  }
}

/**
 * Translates a single text or an array of texts.
 * Fallbacks to the original text if no translation is available.
 */
export function translate(text: string, lang: "pl" | "en"): string {
  if (lang === "pl" || !text) return text;
  
  const trimmed = text.trim();
  
  // 1. Check custom translations
  const custom = getCustomTranslations();
  if (custom[trimmed] !== undefined) return custom[trimmed];
  if (custom[text] !== undefined) return custom[text];
  
  // 2. Check static translations dictionary
  if (staticTranslations[trimmed] !== undefined) return staticTranslations[trimmed];
  if (staticTranslations[text] !== undefined) return staticTranslations[text];
  
  // 3. Fallback to original text
  return text;
}

/**
 * Translates a position (stanowisko) which can be a string, array, or translatable object.
 */
export function translateStanowisko(stanowisko: any, lang: "pl" | "en"): string {
  if (!stanowisko) return "";
  if (typeof stanowisko === "object" && !Array.isArray(stanowisko)) {
    return stanowisko[lang] || stanowisko["pl"] || stanowisko["en"] || "";
  }
  if (Array.isArray(stanowisko)) {
    return stanowisko.map(s => translateStanowisko(s, lang)).join(", ");
  }
  return translate(stanowisko, lang);
}
